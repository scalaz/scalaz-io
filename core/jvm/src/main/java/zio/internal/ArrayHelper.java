package zio.internal;

public abstract class ArrayHelper {
    public static byte getByte(byte[] array, int i) {
        return array[i];
    }

    public static char getChar(char[] array, int i) {
        return array[i];
    }

    public static short getShort(short[] array, int i) {
        return array[i];
    }

    public static int getInt(int[] array, int i) {
        return array[i];
    }

    public static long getLong(long[] array, int i) {
        return array[i];
    }

    public static float getFloat(float[] array, int i) {
        return array[i];
    }

    public static double getDouble(double[] array, int i) {
        return array[i];
    }

    public static boolean getBoolean(boolean[] array, int i) {
        return array[i];
    }
}
